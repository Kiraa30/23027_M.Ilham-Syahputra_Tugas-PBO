    private void tombolJumlahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tombolJumlahActionPerformed
        // TODO add your handling code here:
        switch (pilih) {
            
            case 1:
                bilangan2 = Double.parseDouble(angka);
                jumlah = bilangan1 + bilangan2;
                angka = Double.toString(jumlah);
                Layar.setText(angka);
                break;
                
            case 2:
                bilangan2 = Double.parseDouble(angka);
                jumlah = bilangan1 - bilangan2;
                angka = Double.toString(jumlah);
                Layar.setText(angka);
                break;
                
            case 3:
                bilangan2 = Double.parseDouble(angka);
                jumlah = bilangan1 * bilangan2;
                angka = Double.toString(jumlah);
                Layar.setText(angka);
                break;
                
            case 4:
                bilangan2 = Double.parseDouble(angka);
                jumlah = bilangan1 / bilangan2;
                angka = Double.toString(jumlah);
                Layar.setText(angka);
                break;
            default:
        }
    }//GEN-LAST:event_tombolJumlahActionPerformed

