    private void butSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_butSimpanActionPerformed
        try {
            double nilai1 = Double.parseDouble(fieldNilai1.getText());
            double nilai2 = Double.parseDouble(fieldNilai2.getText());
            double nilai3 = Double.parseDouble(fieldNilai3.getText());
            double nilai4 = Double.parseDouble(fieldNilai4.getText());
            double nilai5 = Double.parseDouble(fieldNilai5.getText());

            double nilaiAkhir = (nilai1 * 0.10) + (nilai2 * 0.15) + (nilai3 * 0.25) + (nilai4 * 0.15) + (nilai5 * 0.35);

            fieldNilaiAkhir.setText(String.valueOf(nilaiAkhir));

            dataMahasiswa.insertData(
                fieldNpm.getText(),
                fieldNama.getText(),
                fieldAlamat.getText(),
                fieldMataKuliah.getText(),
                nilaiAkhir
        );

        viewDataTable();
        clearTextField();
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Masukkan nilai dalam format yang benar.", "Error", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_butSimpanActionPerformed

