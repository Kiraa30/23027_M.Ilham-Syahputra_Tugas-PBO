    public final void viewDataTable(){
        String [] namakolom = {"NPM", "Nama", "Alamat", "Mata Kuliah", "Nilai Akhir"};
        Object [][] objekmahasiswa = new Object[dataMahasiswa.tambahALL().size()][5];
        int i = 0;
        for (DataMahasiswa mhs: dataMahasiswa.tambahALL()) {
            String arrayMahasiswa[] = {
                mhs.tambahnpm(),
                mhs.tambahnama(),
                mhs.tambahalamat(),
                mhs.tambahMataKuliah(),
                String.valueOf(mhs.tambahNilaiAkhir())
            };
            objekmahasiswa[i] = arrayMahasiswa;
            i++;
        }
        modelMahasiswa = new DefaultTableModel(objekmahasiswa, namakolom);
        TBMahasiswa.setModel(modelMahasiswa);
        }
    
